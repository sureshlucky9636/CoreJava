package corejava2;

public class PatternSix {

	public static void main(String[] args) {
		// Pattern Six
		int n=5,k=1;
          for(int i=1; i<=n;i++)
          {
        	  for(int j=1;j<=n;j++)
        	  {
        		  System.out.print(" "+k);
        		  k++;
        	  }
        	  System.out.println();
          }
	}

}
